def calculate_edge_density(img):
    """Tính mật độ đường nét (brushwork complexity)"""
    gray = cv2.cvtColor(img, cv2.COLOR_BGR2GRAY)
    # Dùng Canny để tìm cạnh (nét vẽ)
    edges = cv2.Canny(gray, 100, 200)
    # Tính tỷ lệ pixel cạnh trên tổng số pixel
    density = np.sum(edges) / (img.shape[0] * img.shape[1])
    return density

def extract_features(image_path, k=5):
    """
    Hàm cốt lõi: Đọc ảnh -> Resize -> K-Means Clustering -> Lấy bảng màu
    """
    img = cv2.imread(image_path)
    if img is None: return None

    # 1. Tính độ phức tạp đường nét (để phân tích thêm)
    edge_density = calculate_edge_density(img)

    # 2. Xử lý màu sắc
    img_rgb = cv2.cvtColor(img, cv2.COLOR_BGR2RGB)
    img_small = cv2.resize(img_rgb, (100, 100))
    pixels = img_small.reshape(-1, 3)

    # Dùng K-Means để tìm k màu đại diện
    kmeans = KMeans(n_clusters=k, n_init=10, random_state=42)
    kmeans.fit(pixels)

    colors = kmeans.cluster_centers_
    labels = kmeans.labels_

    counts = np.bincount(labels)
    proportions = counts / len(pixels)

    sorted_idx = np.argsort(proportions)[::-1]
    sorted_colors = colors[sorted_idx]
    sorted_props = proportions[sorted_idx]

    # Tạo vector đặc trưng màu: [R1, G1, B1, %1, R2, G2, B2, %2, ...]
    feature_vector = []
    for i in range(k):
        feature_vector.extend(sorted_colors[i])
        feature_vector.append(sorted_props[i])

    return np.array(feature_vector), edge_density

