def perform_eda():
    print(" PHÂN TÍCH DỮ LIỆU (EDA) & ĐÁNH GIÁ ĐỘ CHÍNH XÁC")

    if len(X_data) == 0: return

    # 1. Thống kê số lượng
    unique, counts = np.unique(y_labels, return_counts=True)
    names = [c["name"] for c in dataset_config.values()]

    plt.figure(figsize=(12, 4))
    plt.subplot(1, 3, 1)
    sns.barplot(x=names, y=counts, palette="viridis")
    plt.title("Số lượng tranh mỗi họa sĩ")
    plt.xticks(rotation=45)


    # 4. Chấm điểm độ chính xác
    print("\n--- ĐANG CHẤM ĐIỂM MÔ HÌNH (Cross-Validation 80/20) ---")
    X_train, X_test, y_train, y_test = train_test_split(X_data, y_labels, test_size=0.2, random_state=42, stratify=y_labels)

    val_knn = KNeighborsClassifier(n_neighbors=min(5, len(X_train)), weights='distance')
    val_knn.fit(X_train, y_train)
    y_pred = val_knn.predict(X_test)

    acc = accuracy_score(y_test, y_pred)
    print(f" ĐỘ CHÍNH XÁC TỔNG QUÁT: {acc*100:.2f}%")
    print("\nChi tiết báo cáo:")
    print(classification_report(y_test, y_pred, target_names=names))

    # Ma trận nhầm lẫn
    cm = confusion_matrix(y_test, y_pred)
    plt.figure(figsize=(6, 5))
    sns.heatmap(cm, annot=True, fmt='d', cmap='Blues', xticklabels=names, yticklabels=names)
    plt.title("Ma trận nhầm lẫn (Confusion Matrix)")
    plt.xlabel("Máy dự đoán")
    plt.ylabel("Thực tế")
    plt.show()

# Chạy phân tích EDA
perform_eda()

